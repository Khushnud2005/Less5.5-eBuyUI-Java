package uz.exemple.less55_ebuyui_java.model;

public class Favorite {

    private String title;
    private int image;

    public Favorite(String title, int image) {
        this.title = title;
        this.image = image;
    }

    public String getTitle() {
        return title;
    }

    public int getImage() {
        return image;
    }
}
