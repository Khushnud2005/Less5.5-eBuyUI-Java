package uz.exemple.less55_ebuyui_java.model;

public class WinterModel {
    private int image;
    private String title;

    public WinterModel(int image, String title) {
        this.image = image;
        this.title = title;
    }

    public int getImage() {
        return image;
    }

    public String getTitle() {
        return title;
    }
}
